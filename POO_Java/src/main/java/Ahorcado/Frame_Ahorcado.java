 Ahorcado ahorcado = new Ahorcado();
    public void mostrarPalabra(){
    lblPalabra.setText("");

    for (int i = 0; i < ahorcado.adivinada.length; i++) {
            if (ahorcado.adivinada[i].equals("")) {
                lblPalabra.setText(lblPalabra.getText()+" _ ");
              
            } else {
                lblPalabra.setText(lblPalabra.getText()+" "+ahorcado.adivinada[i].toUpperCase()+" ");
                
            }
        
        }
 }
